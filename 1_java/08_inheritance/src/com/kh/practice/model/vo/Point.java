package com.kh.practice.model.vo;

public class Point {

	
	private int x;
	private int y;
	
	public Point() {
	}
	
	public Point(int x, int y) {
		this.x=x;
		this.y=y;
	}
	
	public int getx() {
		return x;
	}
	
	public void setx(int x) {
		this.x=x;
	}
	
	public int gety() {
		return y;
	}
	
	public void sety(int y) {
		this.y=y;
	}
}
